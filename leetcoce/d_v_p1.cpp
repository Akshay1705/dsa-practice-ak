#include <iostream>
#include <string>

using namespace std;

int main() {
    // Data Types
    int integerVariable = 10;
    float floatVariable = 3.14;
    string stringVariable = "Hello, World!";
    bool booleanVariable = true;

    cout << "Data Types:" << endl;
    cout << "Integer: " << integerVariable << endl;
    cout << "Float: " << floatVariable << endl;
    cout << "String: " << stringVariable << endl;
    cout << "Boolean: " << booleanVariable << endl;

    // Variables
    int x = 5;
    int y = 8;

    cout << "\nVariables:" << endl;
    cout << "x: " << x << endl;
    cout << "y: " << y << endl;

    return 0;
}